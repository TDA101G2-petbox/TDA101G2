package com.store.service.model;

public class ServiceVO {
	private String service_id;
	private String store_id;
	private String service_detail;
	private Integer service_price;
	private Integer service_limit;
	private Integer service_time;
	private Integer service_state;
	public String getService_id() {
		return service_id;
	}
	public void setService_id(String service_id) {
		this.service_id = service_id;
	}
	public String getStore_id() {
		return store_id;
	}
	public void setStore_id(String store_id) {
		this.store_id = store_id;
	}
	public String getService_detail() {
		return service_detail;
	}
	public void setService_detail(String service_detail) {
		this.service_detail = service_detail;
	}
	public Integer getService_price() {
		return service_price;
	}
	public void setService_price(Integer service_price) {
		this.service_price = service_price;
	}
	public Integer getService_limit() {
		return service_limit;
	}
	public void setService_limit(Integer service_limit) {
		this.service_limit = service_limit;
	}
	public Integer getService_time() {
		return service_time;
	}
	public void setService_time(Integer service_time) {
		this.service_time = service_time;
	}
	public Integer getService_state() {
		return service_state;
	}
	public void setService_state(Integer service_state) {
		this.service_state = service_state;
	}
	
	
}
